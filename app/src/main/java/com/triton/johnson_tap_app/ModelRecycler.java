package com.triton.johnson_tap_app;

public class ModelRecycler {

    private String urtno, bank_details, amount, customer_name, ifsc_code, balance_amt ;

    public String getUrtno() {
        return urtno;
    }

    public void setUrtno(String urtno) {
        this.urtno = urtno;
    }

    public String getBank_details() {
        return bank_details;
    }

    public void setBank_details(String bank_details) {
        this.bank_details = bank_details;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getCustomer_name() {
        return customer_name;
    }

    public void setCustomer_name(String customer_name) {
        this.customer_name = customer_name;
    }

    public String getIfsc_code() {
        return ifsc_code;
    }

    public void setIfsc_code(String ifsc_code) {
        this.ifsc_code = ifsc_code;
    }

    public String getBalance_amt() {
        return balance_amt;
    }

    public void setBalance_amt(String balance_amt) {
        this.balance_amt = balance_amt;
    }

}
